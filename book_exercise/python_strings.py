#!/usr/bin/python3.4

#Single quote
print('play football')

#Use \' to escape single quote
print('doesn\'t')

#Or use double quotes instead
print("doesn't")

print('\"Yes\", \"He said\"')
print('\"Yes\"')
print('\"He said\"')

print("He's a Doctor")
print('He\'s a Doctor\n')

#The \n means a new empty line or space between paragraph. And they must be inside the quote sign.

print('c:\wonderful\newyork')
print("""IF YOU DON'T WANT THIS TO HAPPEN,
 JUST PUT r BEFORE THE QUOTE SIGN\n""")

print(r'c:\wonderful\newyork')

